CREATE TABLE SURVEYGROUP (
	SURVEYGROUPID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY, 
	GUID VARCHAR(4000), 
	CREATEDDATE TIMESTAMP, 
	CREATEDBYUSERID BIGINT, 
	LASTUPDATE TIMESTAMP, 
	LASTUPDATEBYUSERID BIGINT, 
	CONSTRAINT PK_SURVEYGROUPID PRIMARY KEY (SURVEYGROUPID)
);

CREATE TABLE SURVEYGROUPDETAIL (
	SURVEYGROUPDETAILID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY, 
	SURVEYGROUPID BIGINT, 
	SURVEYID BIGINT, 
	SURVEYNAME VARCHAR(4000), 
	CREATEDDATE TIMESTAMP, 
	COMPLETEDDATE TIMESTAMP, 
	LASTUPDATE TIMESTAMP, 
	CONSTRAINT PK_SURVEYGROUPDETAILID PRIMARY KEY (SURVEYGROUPDETAILID), 
	CONSTRAINT FK_SURVEYGROUPDETAIL_SURVEYGROUPID FOREIGN KEY (SURVEYGROUPID) REFERENCES "SURVEYGROUP" ("SURVEYGROUPID"), 
	CONSTRAINT FK_SURVEYGROUPDETAIL_SURVEYID FOREIGN KEY (SURVEYID) REFERENCES "SURVEY" ("SURVEYID")
);

CREATE TABLE SURVEYGROUPQUESTIONANSWER (
	SURVEYGROUPQUESTIONANSWERID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY, 
	SURVEYGROUPDETAILID BIGINT, 
	SURVEYQUESTIONID BIGINT, 
	SURVEYQUESTIONANSWEROPTIONID BIGINT, 
	ANSWERDECIMAL DOUBLE, 
	ANSWERINTEGER BIGINT, 
	ANSWERDATE DATE, 
	ANSWERDATETIME TIMESTAMP, 
	ANSWERTEXT VARCHAR(4000), 
	ANSWERBOOLEAN BOOLEAN, 
	ANSWERBLOB BLOB(1048576), 
	LASTUPDATE TIMESTAMP, 
	CONSTRAINT PK_SURVEYGROUPQUESTIONANSWERID PRIMARY KEY (SURVEYGROUPQUESTIONANSWERID), 
	CONSTRAINT FK_SURVEYGROUPDETAIL_SURVEYGROUPDETAILID FOREIGN KEY (SURVEYGROUPDETAILID) REFERENCES "SURVEYGROUPDETAIL" ("SURVEYGROUPDETAILID")
);